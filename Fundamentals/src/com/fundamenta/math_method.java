package com.fundamenta;

public class math_method {


    public static void main(String[] args) {
        double x = 3;
        double y = Math.round(Math.random()*10);
        double z = Math.sqrt(x);
        double m = Math.pow(x, 2);
        double n = Math.sin(Math.sqrt(3)/2);
        double pi = Math.PI;
        double e = Math.E;
        System.out.println(y+" \n"+z+" \n"+m +" \n"+n +" \n"+pi +" \n"+e);
    }


}
